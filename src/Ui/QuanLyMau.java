    public void resest(){
        txtId.setText("");
        txtName.setText("");
        index = -1;
       
    }
    
    public void loadTable(){
        DefaultTableModel model = (DefaultTableModel) tblColor.getModel();
        model.setRowCount(0);
        for(Color x: listColor){
            model.addRow(new Object[]{x.getTenMau()});
        }
        
    }
    
    public void display(int index){
        txtId.setText(String.valueOf(listColor.get(index).getMaMau()));
        txtName.setText(listColor.get(index).getTenMau());
    }

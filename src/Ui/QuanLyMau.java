    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(txtName.getText().equals("")){
        BoxDiaglog.alert(this, "Không được để trống!");
            }
        String name = txtName.getText();
        Color color = new Color();
        color.setTenMau(name);
        
        if(index<0){
            service.insertColor(color);
            BoxDiaglog.alert(this, "Thêm mới thành công!");
            listColor.add(color);
        }else{
            color.setMaMau(Integer.parseInt(txtId.getText()));
            service.updateColor(color);
            listColor.set(index, color);
            BoxDiaglog.alert(this, "Sửa thành công!");
        }
        loadTable();
        resest();
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblColorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblColorMouseClicked
        // TODO add your handling code here:
        index = tblColor.getSelectedRow();
        display(index);
    }//GEN-LAST:event_tblColorMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(index<0){
            BoxDiaglog.alert(this, "Chọn màu cần xóa!");
        }else{
            int id = Integer.parseInt(txtId.getText());
            service.softDelete(id);
            listColor.remove(index);
            loadTable();
            BoxDiaglog.alert(this, "Xóa thành công!");
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


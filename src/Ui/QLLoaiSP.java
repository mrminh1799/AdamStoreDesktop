    public void loadTable(){
        DefaultTableModel model = (DefaultTableModel) tblLoaiSP.getModel();
        model.setRowCount(0);
        for(Category x: listCategorys){
            model.addRow(new Object[]{x.getTenTheLoai()});   
        }
    }
    
